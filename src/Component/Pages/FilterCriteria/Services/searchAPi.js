import { createApi,fetchBaseQuery} from "@reduxjs/toolkit/query/react";

export const searchApi = createApi({
         reducerPath: "searchApi",
         baseQuery: fetchBaseQuery({
           baseUrl: "http://5.180.148.40:9006/api/das/search",
         }),
         endpoints: builder => ({
           searchData: builder.mutation({
             query: input => ({
               url: "http://5.180.148.40:9006/api/das/search",
               method: "POST",
               body: { input }
             })
           })
         })
       });

export const { useSearchDataMutation } = searchApi;
export default searchApi;
export const searchApiReducer = searchApi.reducer;
 